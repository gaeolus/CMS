package org.resl.gs1.cms.model;

public class Slave {
	String id;
	String bizLocation;
	String writePoint;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBizLocation() {
		return bizLocation;
	}
	public void setBizLocation(String bizLocation) {
		this.bizLocation = bizLocation;
	}
	public String getWritePoint() {
		return writePoint;
	}
	public void setWritePoint(String writePoint) {
		this.writePoint = writePoint;
	}
	
	

}
